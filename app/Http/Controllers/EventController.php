    {
        $request->validate([
            'title' => 'required|string',
            'date' => 'required',
            'calendar_id' => 'required'
        ]);
        $calendar = Event::create([
            'title' => $request->input('title'),
            'date' => $request->input('date'),
            '' => $request->input('date')
        ]);
        return response([
            'message' => 'Calendar added!',
            'calendar' => $calendar
        ]);
    }

